const delay = (ms: number) => new Promise(r => setTimeout(r, ms));

async function fetchCoverUrl(title: string, author: string): Promise<string | null> {
  const attempts = [
    { q: `${title} ${author}` },
    { title, limit: '5' },
    { q: title.replace(/^(The|A|An)\s+/i, '').split(/[:\-,]/)[0].trim() },
  ];

  for (const params of attempts) {
    try {
      const qs = new URLSearchParams({ ...params, fields: 'cover_i', limit: params.limit || '3' });
      const res = await fetch(`https://openlibrary.org/search.json?${qs}`);
      if (!res.ok) continue;
      const data = await res.json();
      const doc = data.docs?.find((d: any) => d.cover_i);
      if (doc?.cover_i) return `https://covers.openlibrary.org/b/id/${doc.cover_i}-L.jpg`;
      await delay(100);
    } catch {
      continue;
    }
  }
  return null;
}

